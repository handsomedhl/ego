package com.ego.item.pojo;

/**
 * 父菜单结点类型
 * @author hl43674824
 *
 */
public class PortalMenuNode {
	private String u;
	private String n;
	private Object i;
	public String getU() {
		return u;
	}
	public void setU(String u) {
		this.u = u;
	}
	public String getN() {
		return n;
	}
	public void setN(String n) {
		this.n = n;
	}
	public Object getI() {
		return i;
	}
	public void setI(Object i) {
		this.i = i;
	}
}
